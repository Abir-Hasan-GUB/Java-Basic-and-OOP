public class MathDemo {
    public static void main(String[] args) {
        System.out.println("Absulot of -9.5 :"+Math.abs(-9.5));
        System.out.println("Square root: "+Math.sqrt(25));
        System.out.println("Power: "+Math.pow(2,3));
        System.out.println("PI : "+Math.PI);
        System.out.println("Maximum : "+Math.max(3, -2));
        System.out.println("Minimum : "+Math.min(3, -2));
        System.out.println("Exponantial : "+Math.exp(3));
        System.out.println("Ciling : "+Math.ceil(-5.6));
        System.out.println("Floor : "+Math.floor(-5.6));
        
        
    }
}
