public class For_Loop__Demo {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i+" Bangladesh is My Country"); //print 10 time
        }
        for (int i = 10; i >= 1; i--) {     // Print 1 to 10 revarge wise
            System.out.println(i);
        }
        for (int i = 2; i <= 100; i=i+2) {    // Print Even number
            System.out.println(i);
        }
    }
}
