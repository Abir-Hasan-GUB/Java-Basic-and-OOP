public class Math_Demo {
    public static void main(String[] args) {
        int x = 2, y = 3;
        System.out.println("Power of X of Y is: "+Math.pow(x, y));
        
        System.out.println("Maximum of X of Y is: "+Math.max(x, y));
        
        System.out.println("Minimum of X of Y is: "+Math.min(x, y));
        
        System.out.println("Absilout of -8.5 is: "+Math.abs(-8));
        
        System.out.println("Round of 2.510 is: "+Math.round(2.510));
    }
}
